SELECT
DRIVER_ID, COUNT(CAR_ID) AS CARS
FROM
TRAVELS
GROUP BY DRIVER_ID
ORDER BY CARS DESC;

SELECT
DRIVER_ID, CARS
FROM
(SELECT
T.DRIVER_ID AS DRIVER_ID, COUNT(T.CAR_ID) AS CARS
FROM
TRAVELS T
GROUP BY 
T.DRIVER_ID) AS CCARS
WHERE CARS > 2
ORDER BY 
CARS DESC;


