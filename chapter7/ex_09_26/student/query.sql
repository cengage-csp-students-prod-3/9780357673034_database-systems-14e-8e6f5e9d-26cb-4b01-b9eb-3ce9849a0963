SELECT
C.CUS_CODE, 
I.INV_NUMBER,
P.P_DESCRIPT,
L.LINE_UNITS AS UNITS BOUGHT,
L.LINE_PRICE AS UNIT PRICE,
ROUND(L.LINE_PRICE * L.LINE_UNITS,2) AS SUBTOTAL
FROM
CUSTOMER C 
JOIN
INVOICE I
ON
C.CUS_CODE = I.CUS_CODE
JOIN
Ch07_SaleCoLINE L
ON 
L.INV_NUMBER = I.INV_NUMBER
JOIN
PRODUCT P
ON
L.P_Code = P.P_CODE
ORDER BY 
C.CUS_CODE, I.INV_NUMBER, P.P_DESCRIPT;