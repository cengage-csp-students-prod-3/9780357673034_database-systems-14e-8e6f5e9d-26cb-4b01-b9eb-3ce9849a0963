USE Ch07_SaleCo;

WITH BALANCES AS (
    SELECT
        ROUND(MIN(C.CUS_BALANCE),2) AS `MINIMUM BALANCES`,
        ROUND(MAX(C.CUS_BALANCE),2) AS `MAXIMUM BALANCES`,
        ROUND(AVG(C.CUS_BALANCE),2) AS `AVERAGE BALANCES`
    FROM
        CUSTOMER AS C
)
SELECT
    B.`MINIMUM BALANCES`,
    B.`MAXIMUM BALANCES`,
    B.`AVERAGE BALANCES`
FROM
   BALANCES AS B;  