SELECT
    E.EMP_NUM,
    E.EMP_FNAME,
    E.EMP_LNAME,
    E.EMP_EMAIL,
    SUM(LL.LINE_QTY) AS TOTAL
FROM
    LGEMPLOYEE E
JOIN
    LGINVOICE I ON E.EMP_NUM = I.EMPLOYEE_ID
JOIN
    LGLINE LL ON I.INV_NUM = LL.INV_NUM
JOIN
    LGPRODUCT P ON LL.PROD_SKU = P.PROD_SKU
JOIN
    LGBRAND B ON P.BRAND_ID = B.BRAND_ID
WHERE
    B.BRAND_NAME = 'BINDER PRIME'
    AND I.INV_DATE BETWEEN '2021-11-01' AND '2021-12-05'
GROUP BY
    E.EMP_NUM, E.EMP_FNAME, E.EMP_LNAME, E.EMP_EMAIL
ORDER BY
    TOTAL DESC,
    E.EMP_LNAME ASC
LIMIT 2;