  C.CUST_CODE,
  C.CUST_FNAME,
  C.CUST_LNAME
FROM
  LGCUSTOMER C
JOIN
  LGINVOICE I ON C.CUST_CODE = I.CUST_CODE
WHERE
  I.EMPLOYEE_ID = 83649
INTERSECT
SELECT
  C.CUST_CODE,
  C.CUST_FNAME,
  C.CUST_LNAME
FROM
  LGCUSTOMER C
JOIN
  LGINVOICE I ON C.CUST_CODE = I.CUST_CODE
