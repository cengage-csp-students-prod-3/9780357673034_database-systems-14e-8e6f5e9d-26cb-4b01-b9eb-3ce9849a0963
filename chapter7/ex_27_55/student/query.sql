
SELECT
C.CUST_FNAME,
C.CUST_LNAME,
C.CUST_STREET,
C.CUST_CITY,
C.CUST_STATE,
C.CUST_ZIP
FROM
LGCUSTOMER C
JOIN
LGINVOICE I
ON
C.CUST_CODE = I.CUST_CODE
JOIN
LGLINE L
ON
I.INV_NUM = L.INV_NUM
JOIN
LGPRODUCT P
ON
L.PROD_SKU = P.PROD_SKU
JOIN
LGBRAND B
ON
P.BRAND_ID = B.BRAND_ID
WHERE 
B.BRAND_NAME = "FORESTERS BEST"
AND
I.INV_DATE BETWEEN "2021/07/15" AND "2021/07/31"
ORDER BY
C.CUST_STATE, C.CUST_LNAME, C.CUST_FNAME;