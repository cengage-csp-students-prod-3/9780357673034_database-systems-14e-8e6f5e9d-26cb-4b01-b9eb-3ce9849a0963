SELECT
    B.BRAND_ID,
    B.BRAND_NAME,
    B.BRAND_TYPE,
    ROUND(AVG(P.PROD_PRICE), 2) AS AVERAGE_PRODUCT_PRICE
FROM
    LGBRAND B
JOIN
    LGPRODUCT P ON B.BRAND_ID = P.BRAND_ID
WHERE
    B.BRAND_ID = 29 -- This line filters for the specific brand ID
GROUP BY
    B.BRAND_ID,
    B.BRAND_NAME,
    B.BRAND_TYPE;